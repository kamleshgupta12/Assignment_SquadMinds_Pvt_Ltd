  function findRoutes(grid) {
    for (let i = 0; i < rows; i++) {
      grid[i][0] = 1;
    }

    for (let j = 0; j < cols; j++) {
      grid[0][j] = 1;
    }

    for (let i = 1; i < rows; i++) {
      for (let j = 1; j < cols; j++) {
        grid[i][j] = grid[i - 1][j] + grid[i][j - 1];
      }
    }

    return grid[rows - 1][cols - 1];
  }

